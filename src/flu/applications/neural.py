    # ── Internal helpers ──────────────────────────────────────────────────

    @staticmethod
    def _choose_n(shape: Tuple[int, ...]) -> int:
        """
        Choose the smallest FLU order n ≥ max(shape).

        STATUS: DESIGN INTENT — n only needs to be ≥ every individual dimension
        so that cropping to the requested shape is always valid.
        """
        n = max(shape)
        if n < 2:
            n = 2
        return n
